function multiplicacionClasica(num1,num2){
    return num1 * num2;
}
console.log(multiplicacionClasica(2,5));

const multiplicacionArrow = (num1,num2) => {
  return num1 * num2;
};
console.log(multiplicacionArrow(2, 5)); 

function cuadradoClasica(num) {
  return num * num;
}
console.log(cuadradoClasica(3)); 

const cuadradoArrow = (num) => {
    return num * num;
}
console.log(cuadradoArrow(3));